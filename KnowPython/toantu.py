# Toán tử trong python

# Toán tử số học
a = 10
b = 5
print(a + b) # Cộng
print(a - b) # Trừ
print(a * b) # Nhân
print(a / b) # Chia
print(a // b) # Chia lấy nguyên
print(a % b) # Chia lấy dư
print(a ** b) # Lũy thừa
print(a & b) # Phép AND
print(a | b) # Phép OR
print(a ^ b) # Phép XOR
print(a << b) # Dịch trái
print(a >> b) # Dịch phải
print(a == b) # So sánh bằng
print(a != b) # So sánh khác
print(a > b) # So sánh lớn hơn
print(a < b) # So sánh nhỏ hơn
print(a >= b) # So sánh lớn hơn hoặc bằng
print(a <= b) # So sánh nhỏ hơn hoặc bằng
print(a is b) # So sánh địa chỉ
print(a is not b) # So sánh địa chỉ khác
print(a in b) # Kiểm tra có trong
print(a not in b) # Kiểm tra không có trong
